# Built-in Data Types in Python

# Number data types:

# Integer: Whole numbers
# Float: decimal numbers
# Complex: number with real and imaginary parts

print(45e3)
print(5.6)
print(0)
print(-6.5)
print(2 + 5.4j)

# check the datatype: type()
print(type(56))
print(type(5.6j))
print(type(5e6))
print(type(56.9))

# String datatype:
print(type("Hello everyone!"))
print(type('Hi everyone!'))

x = 3.14
print(type(x))