# User-defined Data Types in Python

# Assigning multiple values to multiple variables
num1, num2, num3 = 10, 20, 30
# num1 = 10
# num2 = 30
# num3 = 50

# Printing multiple values in single line
print(num1, num2, num3, sep="--")

# print(num1)
# print(num2)
# print(num3)

# Assigning single value to multiple variables
x = y = z = 45
print(x,y,z)

# Variables to be meaningfully named:
person_name = "Preveen"
person_age = 25
print(person_name , "is", person_age, "years old.")


# Variable naming conventions (styles)
# for variables with multiple words:
person_id = 70  # snake_case
personId = 70 # camelCase
PersonId = 70 # PascalCase
print(person_id, personId, PersonId)

# constants in python
PI = 3.14
E=2.71828
print(PI)
print(E)
