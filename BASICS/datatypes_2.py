# Built-in Data Types in Python 

# Boolean datatype
print(True)
print(False)

print(type(True))
print(type(False))

# None datatype
print(None)
print(type(None))


# Sequence data types:
# Strings: Immutable type " " / ' '
string1 = "Hello, World!" 
print(string1)
print(type(string1))

# Lists: Mutable type []
list1 = [1,2,3,4,5]
print(list1)
print(type(list1))

# Tuples: Immutable type ()
tuple1 = (6,7,8,9,10)
print(tuple1)
print(type(tuple1))

# Sets: Mutable type {}
set1 = {10, 20.0, "30", (40,50)}
print(set1)
print(type(set1))

# Maps: Mutable type {key:value} pair
dict1 = {   
    "name": "Alice",
    "age": 30
}
print(dict1)
print(type(dict1))